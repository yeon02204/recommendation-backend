package com.example.recommendation.domain.home.answer;

/**
 * 사용자 발화의 주된 의도 분류
 *
 * [역할]
 * - 사용자 응답이 무엇을 의미하는지 종류만 판단
 * - 슬롯과 무관
 *
 * [절대 금지]
 * - 슬롯 판단 ❌
 * - 상태 변경 ❌
 */
public enum AnswerIntent {
    
    /**
     * 실제 답변
     * 예: "친한 친구요", "30만원 정도요"
     */
    ANSWER,
    
    /**
     * 모르겠음 / 애매함
     * 예: "모르겠어요", "잘 모르겠는데요"
     */
    UNKNOWN,
    
    /**
     * 대답 거부
     * 예: "그냥 넘어갈게요", "상관없어요"
     */
    REFUSAL,
    
    /**
     * 새로운 화제 제시
     * 예: "다른 거 추천해주세요", "처음부터 다시 할래요"
     */
    CONTEXT_SHIFT,
    
    /**
     * 의미 없음 / 잡담
     * 예: "음...", "그러니까..."
     */
    NOISE
}